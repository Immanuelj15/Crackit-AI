const mongoose = require('mongoose');

const resultSchema = new mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    topic: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Topic',
        required: true
    },
    category: {
        type: String,
        enum: ['quant', 'logical', 'verbal'],
        required: true
    },
    score: {
        type: Number,
        required: true
    },
    totalQuestions: {
        type: Number,
        required: true
    },
    correctAnswers: {
        type: Number,
        required: true
    },
    wrongAnswers: {
        type: Number,
        required: true
    },
    timeTaken: {
        type: Number, // In seconds
        required: true
    },
    accuracy: {
        type: Number, // Percentage
        required: true
    },
    averageTimePerQuestion: {
        type: Number, // Seconds
        required: true
    },
    aiFeedback: {
        type: String // Feedback generated by AI
    }
}, { timestamps: true });

module.exports = mongoose.model('Result', resultSchema);
